long double half_life::get(atom a)
{
	if(a.mass() <= 0 && (a.protons() == -1 || a.protons() == 1)) return 22;

	auto it = lookup.find(a);

	if(it == lookup.end())
	{
		return -7.5;
	}

	// map from 0 -> 63 to 10^-7.5 -> 10^22
	return ((long double)it->second / 63.0 * 29.5) - 7.5;
}

